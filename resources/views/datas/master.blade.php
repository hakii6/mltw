<h2><b>通常來說，為何要突破這張卡？</b></br></h2>
<ol>
<li><b>PST 卡（上位限定）</b>　＆　<b>SSR 卡</b>　達到四突可以獲得<b>異色衣服</b></li>
<li><b>突破</b>　可以增加隊伍<b>戰力</b></li>
</ol>
</br>

<h2><b>日版內容</b></h2>
<ol>
    <li><b>五突</b>預計會在 2021 年底開放　且<b>非所有卡</b>　只有<b>卡面素質過低的卡</b>（通常是<b>剛開服不久的 SSR 卡</b>）才會開放</li>
    <li>五突所消耗之石頭較前四突<b>多許多</b></li>
    <li>五突可以獲得<b>第二套異色服裝</b>（不同於原始 SSR 服裝　＆　四突 SSR 服裝）</li>
</ol>

</br>
<h3><b>其餘注意事項</b></h3>
<ul>

    <li><b>PST 卡</b>雖然是 SR 卡　但是無法用 SR 卡的方式突破　只能使用<b> PST 石</b>　且必須<b>等待營運開放突破</b>　才能突破</li>
    <li><b>FES 卡</b>　只能使用<b> FES 石</b>突破　SSR 石 ＆ SR 石皆不可</li>
    <li>卡面素質上升為<b>固定各素質零突的 3 %</b></li>

</ul>

